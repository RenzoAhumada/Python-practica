#Logica de la calculadora, trayendo del main los numeros para hacer lo que se pide desde el menu.
#Desde aqui estan todas las funciones de la misma

def sumar(num1, num2):
    return num1 + num2

def resta(num1, num2):
    return num1-num2

def multiplicacion(num1, num2):
    return num1*num2

def divicion (num1, num2):
    return num1/num2       
